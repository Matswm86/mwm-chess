    )
    private val QUEEN = intArrayOf(
        -20, -10, -10, -5, -5, -10, -10, -20,
        -10, 0, 0, 0, 0, 0, 0, -10,
        -10, 0, 5, 5, 5, 5, 0, -10,
        -5, 0, 5, 5, 5, 5, 0, -5,
        0, 0, 5, 5, 5, 5, 0, -5,
        -10, 5, 5, 5, 5, 5, 0, -10,
        -10, 0, 5, 0, 0, 0, 0, -10,
        -20, -10, -10, -5, -5, -10, -10, -20,
    )
    private val KING = intArrayOf(
        -30, -40, -40, -50, -50, -40, -40, -30,
        -30, -40, -40, -50, -50, -40, -40, -30,
        -30, -40, -40, -50, -50, -40, -40, -30,
        -30, -40, -40, -50, -50, -40, -40, -30,
        -20, -30, -30, -40, -40, -30, -30, -20,
        -10, -20, -20, -20, -20, -20, -20, -10,
        20, 20, 0, 0, 0, 0, 20, 20,
        20, 30, 10, 0, 0, 10, 30, 20,
    )

    private fun table(type: PieceType): IntArray = when (type) {
        PieceType.PAWN -> PAWN
        PieceType.KNIGHT -> KNIGHT
        PieceType.BISHOP -> BISHOP
        PieceType.ROOK -> ROOK
        PieceType.QUEEN -> QUEEN
        PieceType.KING -> KING
    }

    private fun pst(type: PieceType, sq: Int, color: Color): Int {
        val idx = if (color == Color.WHITE) (7 - rankOf(sq)) * 8 + fileOf(sq)
        else rankOf(sq) * 8 + fileOf(sq)
        return table(type)[idx]
    }

    /** Score from the perspective of the side to move (for negamax). */
    fun evaluate(b: Board): Int {
        var score = 0
        for (sq in 0..63) {
            val p = b.squares[sq] ?: continue
            val v = VALUE[p.type.ordinal] + pst(p.type, sq, p.color)
            if (p.color == Color.WHITE) score += v else score -= v
        }
        return if (b.sideToMove == Color.WHITE) score else -score
    }
}
